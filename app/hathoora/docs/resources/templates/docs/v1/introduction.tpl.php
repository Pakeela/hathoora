    <a name="philosophy"></a>
    <h2>Philosophy</h2>
    <p>
        This framework is built with following in mind:
    </p>
    <ul>
        <li>Easy to <a href="/docs/v1/configuration#applicationmultiple">support</a> and <a href="/docs/v1/configuration#applicationorganization">organize</a> code for multiple websites/applications (cms, api, sub-projects, sub-domains etc).</li>
        <li>Simple and <a href="/docs/v1/routing">multiple routing</a> without dealing with routing files or compromising <a href="/docs/v1/routing#httpd">webservers rewrites</a>.</li>
        <li>Freedom developing business <a href="/docs/v1/model">model</a> logic because you know what you are doing with raw SQL - no ORM, thank you.</li>
        <li>Support for <a href="/docs/v1/database#advanced">complex database setup</a> - multiple masters, multiple slaves or <a href="/docs/v1/model#modelSAR">sharding</a>.</li>
        <li>Database driven <a href="/docs/v1/translation">translation system</a> because you need to make changes to content without deployments.</li>
        <li>Powerful <a href="/docs/v1/grid">grid</a> system for rapid common "admin" panel development tasks.</li>
        <li>Ability to execute controller code from <a href="/docs/v1/cli">command line</a>.</li>
        <li>YAML based configurations so you can share between php and other scripts (in perl, python, bash etc).</li>
    </ul>

    <a name="name"></a>
    <h2>Where does "Hathoora" come from?</h2>
    <p>
        Hathoora (ہتھوڑا) is an <a href="http://en.wikipedia.org/wiki/Urdu_language" target="_blank">Urdu</a> word meanning hammer. Hammer is a tool meant to deliver an impact, to fit parts, to forge and build things. That is the idea.
    </p>
